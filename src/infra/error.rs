impl CaduceusError {
    /// Wrap a non-token error message in `Other(String)`. This is
    /// the only public constructor that doesn't already have a
    /// variant; callers should prefer the typed variants.
    pub fn other<S: Into<String>>(message: S) -> Self {
        CaduceusError::Other(message.into())
    }

    /// Map a daemon error to the process exit code announced in
    /// `CONTRACTS.md` "CLI contract".
    ///
    /// `run` returns 0 for processed/idle/concurrent/cadence/
    /// rate-limit/cancelled outcomes and 1 for configuration,
    /// corruption, invariant, or unrecovered pipeline failures.
    /// `status` returns 2 for missing state and 1 for corrupt
    /// state. The CLI host code is responsible for selecting the
    /// right exit-code policy per command.
    pub fn exit_code(&self) -> std::process::ExitCode {
        use std::process::ExitCode;
        match self {
            CaduceusError::Cancelled => ExitCode::from(0),
            CaduceusError::RateLimited { .. } => ExitCode::from(0),
            _ => ExitCode::from(1),
        }
    }
}

