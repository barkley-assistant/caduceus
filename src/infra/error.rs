/// Errors produced by the public-voice validator (Task 6.6). Kept
/// separate from [`CaduceusError`] so the validator can run before
/// a corresponding API mutation has been attempted and report a
/// precise reason without conflating with daemon-wide errors.
#[derive(Clone, Debug, Eq, PartialEq, thiserror::Error)]
pub enum VoiceError {
    /// A configured forbidden term was found in the outbound text.
    /// The first match is captured — operators see *which* term
    /// was matched so they can update the allowlist.
    #[error("voice: forbidden term matched: {found:?}")]
    Forbidden { found: String },

    /// The outbound text exceeds the documented size limit for the
    /// channel (PR body, comment, etc.).
    #[error("voice: text exceeds limit of {limit} bytes")]
    TooLong { limit: usize },
}

impl VoiceError {
    /// True when the error is purely about the text content; the
    /// daemon can suppress retry on these and ask the operator to
    /// adjust the source text.
    pub fn is_terminal(&self) -> bool {
        match self {
            VoiceError::Forbidden { .. } => true,
            VoiceError::TooLong { .. } => true,
        }
    }
}

