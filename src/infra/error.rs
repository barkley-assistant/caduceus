//! Typed error hierarchy for the daemon.
//!
//! [`CaduceusError`] is the canonical cross-cutting error type. It is
//! re-exported from `lib.rs` and used by every module. The variant
//! set and the field semantics are pinned by `CONTRACTS.md`
//! "Error contract".
//!
//! [`VoiceError`] is a separate type used by the public-voice
//! validator (Task 6.6). It is intentionally separate from
//! [`CaduceusError`] because the voice validator runs *before* the
//! corresponding API mutation and needs to surface a precise
//! reason; mixing the two would lose that distinction.
//!
//! `Debug` is wired through [`scrub`] so a resolved token can never
//! appear in a panic message, log line, or test failure. Callers
//! must pre-scrub before constructing an error variant — the
//! type's invariant is "no resolved token in any string field".

use std::fmt;
use std::path::PathBuf;

/// All Caduceus errors. The variant set is normative; new errors
/// always go through a `CaduceusError` constructor, never a panic on
/// external data.
///
/// `Debug` is wired through [`scrub`] so a resolved token can never
/// appear in user-visible output. `Display` is generated by
/// thiserror and renders variant fields verbatim — callers must
/// scrub before constructing. The daemon's structured logger and
/// the public-voice validator both pass through `Debug` only.
#[derive(thiserror::Error)]
pub enum CaduceusError {
    #[error("caduceus configuration error: {0}")]
    Config(String),

    #[error(transparent)]
    Io(#[from] std::io::Error),

    #[error(transparent)]
    Json(#[from] serde_json::Error),

    #[error(transparent)]
    Yaml(#[from] serde_yaml::Error),

    #[error(transparent)]
    Http(#[from] reqwest::Error),

    /// Git subprocess failure. The ``operation`` is a short stable
    /// identifier (e.g. ``"commit"``, ``"push"``); ``stderr`` carries
    /// the captured stderr text (already redacted by the caller).
    #[error("git {operation} failed: {stderr}")]
    Git {
        operation: &'static str,
        stderr: String,
    },

    /// GitHub API returned a non-success status.
    #[error("GitHub API status {status}: {message}")]
    GitHubApi { status: u16, message: String },

    /// GitHub rate-limit observation. The numeric ``reset_at`` is
    /// the seconds-until-reset reported by the API; ``remaining``
    /// is the request budget left; ``limit`` is the per-window cap
    /// (None when the API omits it).
    #[error("GitHub rate limited; reset in {reset_at}s (remaining {remaining}/{limit:?})")]
    RateLimited {
        reset_at: u64,
        remaining: u32,
        limit: Option<u32>,
    },

    /// Token resolution failure. The wrapped string is a reason
    /// (``"gh not found"``, ``"gh exited 1"``); never the token.
    #[error("failed to resolve a GitHub token: {0}")]
    TokenResolution(String),

    /// Worker supervision failure. ``context`` is a stable operation
    /// label (``"spawn"``, ``"wait"``, ``"signal"``); ``stderr``
    /// carries the captured error text (already redacted).
    #[error("worker supervision failure during {context}: {stderr}")]
    Worker {
        context: &'static str,
        stderr: String,
    },

    /// Worktree creation/teardown failure. ``context`` is a stable
    /// operation label (``"create"``, ``"destroy"``, ``"gc"``);
    /// ``stderr`` carries the captured error text.
    #[error("worktree {context} failure: {stderr}")]
    Worktree {
        context: &'static str,
        stderr: String,
    },

    /// Queue state transition failure. ``context`` is a stable
    /// operation label (``"claim"``, ``"release"``, ``"complete"``).
    #[error("queue {context} failure: {stderr}")]
    Queue {
        context: &'static str,
        stderr: String,
    },

    /// Push subprocess failure. ``context`` is a stable operation
    /// label (``"ls-remote"``, ``"push"``); ``stderr`` carries the
    /// captured error text (already redacted by the caller).
    #[error("push {context} failure: {stderr}")]
    Push {
        context: &'static str,
        stderr: String,
    },

    /// The remote had a non-ancestor ref under the daemon branch
    /// name. The push is rejected without `--force`; the operator
    /// must reconcile the branch manually.
    #[error("push collision on {branch}: remote {remote_oid}, local {local_oid}")]
    PushCollision {
        branch: String,
        remote_oid: String,
        local_oid: String,
    },

    /// State file is corrupt on disk. The path is preserved verbatim
    /// so the operator can quarantine or inspect the file. The
    /// error variant does NOT auto-delete the file — the daemon's
    /// contract preserves corruption for diagnosis.
    #[error("corrupt state file at {}: {message}", path.display())]
    StateCorrupt { path: PathBuf, message: String },

    /// Reconciliation of an external side effect failed: the
    /// remote marker disagrees with the local checkpoint. The
    /// operator must inspect the run and resolve the conflict.
    #[error("reconciliation failed for {stage}: {details}")]
    ReconciliationFailed { stage: String, details: String },

    /// A conflicting remote marker was found during
    /// reconciliation. The generation transitions to
    /// `NeedsAttention` so the operator can inspect and
    /// resolve.
    #[error("conflicting remote marker for {stage}: expected {expected}, found {actual}")]
    ConflictingMarker {
        stage: String,
        expected: String,
        actual: String,
    },

    /// Scheduler leadership was contended. The context is a stable
    /// operation label (e.g. ``"acquire"``); stderr carries the
    /// captured error detail.
    #[error("scheduler leadership contended: {context}: {stderr}")]
    LeadershipContended {
        context: &'static str,
        stderr: String,
    },

    /// Lease operation failed because the lease is not held, is
    /// expired, or the caller is not the current owner. The context
    /// is a stable label (e.g. ``"renew"``, ``"release"``); stderr
    /// carries the detail.
    #[error("lease {context} failure: {stderr}")]
    LeaseStale {
        context: &'static str,
        stderr: String,
    },

    /// A mutation was attempted with a fencing token that is lower
    /// than the lease's current token, indicating the caller is a
    /// recovered prior holder whose lease has expired but whose
    /// cached token is stale. The error carries both the stale and
    /// current tokens for diagnostics.
    #[error(
        "fencing token regression for {issue_key}: stale token \
         {stale_token} < current token {current_token}"
    )]
    FencingTokenRegression {
        issue_key: String,
        stale_token: u64,
        current_token: u64,
    },

    #[error("operation cancelled")]
    Cancelled,

    /// The worker pool has no available permits. Current depth
    /// and max depth are surfaced so the caller can decide
    /// whether to retry immediately or route to NeedsAttention.
    #[error("pool saturated: {current_depth}/{max_depth} permits held")]
    PoolSaturated { current_depth: u32, max_depth: u32 },

    /// A per-repository exclusion lock is held by another
    /// admission for the same repo key. The caller should
    /// treat this as a transient infrastructure failure.
    #[error("repository exclusion held: {repo_key}")]
    RepositoryExclusionHeld { repo_key: String },

    /// The drain timeout elapsed before all in-flight workers
    /// completed. The listed run IDs had their leases cancelled.
    #[error("drain timed out for run(s): {timed_out_run_ids:?}")]
    DrainTimeout { timed_out_run_ids: Vec<String> },

    /// The circuit is open for the given scope and scope_id.
    /// `retry_after` is the number of seconds until the circuit
    /// transitions to half-open for a probe.
    #[error("circuit open for {scope}:{scope_id}, retry after {retry_after}s (probe_in_flight={probe_in_flight})")]
    CircuitOpen {
        scope: &'static str,
        scope_id: String,
        retry_after: i64,
        probe_in_flight: bool,
    },

    /// The circuit has been open longer than the max degraded age.
    /// The associated work must transition to NeedsAttention.
    #[error("max degraded age exceeded for {scope}:{scope_id}, opened at {opened_at}")]
    MaxDegradedAgeExceeded {
        scope: &'static str,
        scope_id: String,
        opened_at: i64,
    },

    /// The storage root (or a controlled subdirectory) is a
    /// symlink. The daemon refuses to operate on symlinked
    /// storage to prevent TOCTOU escapes.
    #[error("storage root is a symlink: {}", path.display())]
    SymlinkedStorageRoot { path: PathBuf },

    /// A worktree from a previous failed attempt was found at the
    /// requested path. The daemon refuses to reuse a worktree
    /// whose prior lifecycle was not cleanly completed.
    #[error(
        "reuse of failed worktree {run_id} at {}: last state {last_state}",
        worktree_path.display()
    )]
    WorktreeReuseAfterFailure {
        run_id: String,
        worktree_path: PathBuf,
        last_state: String,
    },

    /// A filesystem mode check did not match the expected
    /// permissions. The expected and observed modes are shown in
    /// octal.
    #[error(
        "mode not preserved at {}: expected {expected:o}, observed {observed:o}",
        path.display()
    )]
    ModeNotPreserved {
        path: PathBuf,
        expected: u32,
        observed: u32,
    },

    /// The configured OCI CLI binary was not found in PATH.
    #[error("OCI CLI not found: {cli}")]
    OciCliNotFound { cli: String },

    /// The OCI engine (Docker/Podman daemon) is not reachable.
    #[error("OCI engine unavailable: {detail}")]
    OciEngineUnavailable { detail: String },

    /// The OCI CLI version is incompatible with the daemon's contract.
    #[error("OCI CLI version mismatch: {detail}")]
    OciMismatchedCliVersion { detail: String },

    /// Image pull failed.
    #[error("OCI pull failed for {image}: {stderr}")]
    OciPullFailed { image: String, stderr: String },

    /// Container create step failed.
    #[error("OCI create failed: {context}: {stderr}")]
    OciCreateFailed {
        context: &'static str,
        stderr: String,
    },

    /// Container start step failed.
    #[error("OCI start failed: {context}: {stderr}")]
    OciStartFailed {
        context: &'static str,
        stderr: String,
    },

    /// Container wait step failed.
    #[error("OCI wait failed: {context}: {stderr}")]
    OciWaitFailed {
        context: &'static str,
        stderr: String,
    },

    /// Container stop step failed.
    #[error("OCI stop failed: {context}: {stderr}")]
    OciStopFailed {
        context: &'static str,
        stderr: String,
    },

    /// Container remove step failed.
    #[error("OCI remove failed: {context}: {stderr}")]
    OciRemoveFailed {
        context: &'static str,
        stderr: String,
    },

    /// A mount path not in the declared allow-list was requested.
    #[error("OCI undeclared mount: {path}")]
    OciUndeclaredMount { path: String },

    /// A potential secret leak was detected in OCI output.
    #[error("OCI secret leak suspected at {path}")]
    OciSecretLeakSuspected { path: String },

    /// A secret value was found in OCI output after the container
    /// completed. This is a confirmed leak, not a suspicion.
    #[error("OCI secret leak detected in output for {run_id}")]
    OciSecretLeakDetected { run_id: String },

    /// Trusted-host execution was selected but the operator has not
    /// explicitly acknowledged reduced containment. This fires at
    /// `Config::load` BEFORE any subprocess is spawned, so a daemon
    /// that fails this check never starts the worker.
    #[error("trusted-host execution requires reduced_containment_acknowledged: true in config")]
    ReducedContainmentNotAcknowledged,

    /// A network profile referenced in the run spec was not found in
    /// the configured allow-list.
    #[error("OCI network profile not in configured profiles: {profile}")]
    OciNetworkNotInProfile { profile: String },

    /// A secret was requested but is not in the configured grant list.
    #[error("OCI secret not granted: {name}")]
    OciSecretNotGranted { name: String },

    /// An image reference is tag-only (not pinned by digest).
    #[error("OCI image is not digest-pinned: {reference}")]
    OciImageNotDigestPinned { reference: String },

    /// A required resource limit (CPU, memory, or PIDs) was not set.
    #[error("OCI resource limit required: {resource}")]
    OciResourceLimitRequired { resource: &'static str },

    /// A baseline policy check failed (e.g., engine socket mount detected).
    #[error("OCI baseline violation: {detail}")]
    OciBaselineViolation { detail: String },

    /// No upgrade choice has been persisted for the daemon.
    #[error("OCI upgrade choice is required before running in OCI mode")]
    OciUpgradeChoiceRequired,

    /// Pull policy `Always` is incompatible with digest-pinned images.
    #[error("OCI pull policy incompatible: {detail}")]
    OciPullPolicyIncompatible { detail: String },

    #[error("{0}")]
    Other(String),
}

/// Canonical `Result` alias used everywhere in the daemon.
pub type CaduceusResult<T> = Result<T, CaduceusError>;

