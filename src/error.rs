impl From<std::path::StripPrefixError> for CaduceusError {
    fn from(_: std::path::StripPrefixError) -> Self {
        CaduceusError::Other("strip prefix failed".to_string())
    }
}

// `From<tokio::task::JoinError>` etc. would be added here as the
// runtime grows. Only conversions that lose no information about the
// underlying cause land in this impl block.

impl From<std::num::ParseIntError> for CaduceusError {
    fn from(err: std::num::ParseIntError) -> Self {
        CaduceusError::Other(format!("parse int: {err}"))
    }
}

// ---------------------------------------------------------------------------
// Redaction helper
// ---------------------------------------------------------------------------

/// Replace recognised credential names with `<redacted>` so the
/// returned string is safe to render through `Debug` (and any
/// downstream `tracing` panic message or test failure message).
///
/// The deny-list mirrors the worker-env-allowlist contract: the
/// three canonical credential variables and any prefix wildcard
/// that would expose them. The function is intentionally a
/// standalone helper so every error path that wants to surface a
/// string can apply it.
pub fn scrub(value: &str) -> String {
    if value.is_empty() {
        return value.to_string();
    }
    let mut scrubbed = value.to_string();
    for needle in ["GITHUB_TOKEN", "CADUCEUS_GITHUB_TOKEN", "GH_TOKEN"] {
        let mut search_from = 0usize;
        while let Some(pos) = scrubbed[search_from..].find(needle) {
            let abs = search_from + pos;
            // Boundary check on the left so ``MY_GITHUB_TOKEN`` is
            // not matched.
            if abs > 0 {
                let prev = scrubbed.as_bytes()[abs - 1];
                if prev.is_ascii_alphanumeric() || prev == b'_' {
                    search_from = abs + needle.len();
                    continue;
                }
            }
            let after = abs + needle.len();
            let mut cursor = after;
            while cursor < scrubbed.len() && scrubbed.as_bytes()[cursor] == b' ' {
                cursor += 1;
            }
            if cursor < scrubbed.len() && scrubbed.as_bytes()[cursor] == b'=' {
                cursor += 1;
                while cursor < scrubbed.len() && scrubbed.as_bytes()[cursor] == b' ' {
                    cursor += 1;
                }
            }
            let value_start = cursor;
            let value_end = advance_to_end_of_value(&scrubbed, value_start);
            scrubbed.replace_range(value_start..value_end, "<redacted>");
            search_from = value_start + "<redacted>".len();
        }
    }
    scrubbed
}

fn advance_to_end_of_value(s: &str, start: usize) -> usize {
    let bytes = s.as_bytes();
    if start >= bytes.len() {
        return start;
    }
    let first = bytes[start];
    if first == b'"' || first == b'\'' {
        let quote = first;
        let mut i = start + 1;
        while i < bytes.len() {
            if bytes[i] == b'\\' && i + 1 < bytes.len() {
                i += 2;
                continue;
            }
            if bytes[i] == quote {
                return i + 1;
            }
            i += 1;
        }
        return bytes.len();
    }
    let mut i = start;
    while i < bytes.len() {
        match bytes[i] {
            b' ' | b'\t' | b'\n' | b'\r' | b',' | b';' | b'}' | b']' => break,
            _ => i += 1,
        }
    }
    i
}

// ---------------------------------------------------------------------------
// Tests live in `tests/error_test.rs`.
// ---------------------------------------------------------------------------
#[cfg(test)]
mod inline_tests {
    use super::*;

    #[test]
    fn scrub_redacts_token_in_value() {
        let out = scrub("GITHUB_TOKEN=ghp_xyz");
        assert!(out.contains("<redacted>"));
        assert!(!out.contains("ghp_xyz"));
    }

    #[test]
    fn scrub_leaves_unrelated_strings_intact() {
        assert_eq!(scrub("hello world"), "hello world");
    }
}
